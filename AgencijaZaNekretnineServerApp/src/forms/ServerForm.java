    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        // TODO add your handling code here:
            prop.setProperty("db.url",txtUrl.getText());
            prop.setProperty("db.user",txtUser.getText());
            prop.setProperty("db.password",txtPassword.getText());
            
        try {
            FileOutputStream output = new FileOutputStream("config.properties");
            prop.store(output,"Edited configuration");
            JOptionPane.showMessageDialog(this,"Uspe≈°no izmenjeni parametri!","Izmena uspe≈°na",JOptionPane.INFORMATION_MESSAGE);
            ucitajPodatke();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ServerForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ServerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnSacuvajActionPerformed


