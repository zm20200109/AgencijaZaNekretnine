    private void popuniPodacima() {
        txtBrojURegistru.setText(""+this.agent.getRedniBrojURegistruPosrednika());
        txtImeAgenta.setText(agent.getImeAgenta());
        txtPrezime.setText(agent.getPrezimeAgenta());
        txtAdresaAgenta.setText(agent.getAdresaAgenta());
        txtUspesnostRealizacije.setText(""+agent.getUspesnostRealizacije());
        txtBrojDodeljenihKlijenata.setText(""+agent.getBrojDodeljenihKlijenata());
        
        txtBrojURegistru.setEnabled(false);
        txtImeAgenta.setEnabled(false);
        txtPrezime.setEnabled(false);
        txtAdresaAgenta.setEnabled(false);
        txtUspesnostRealizacije.setEnabled(false);
        txtBrojDodeljenihKlijenata.setEnabled(false);
        JOptionPane.showMessageDialog(this, "Sistem je učitao agenta.", "Pretraga agenta",JOptionPane.INFORMATION_MESSAGE);

    }
