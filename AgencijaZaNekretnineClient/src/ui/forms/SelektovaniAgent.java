    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        try {
            // TODO add your handling code here:
            long broj=Long.parseLong(txtBrojURegistru.getText());
            String ime = txtImeAgenta.getText();
            String prezime = txtPrezime.getText();
            String adresa = txtAdresaAgenta.getText();
            double uspesnost = Double.parseDouble(txtUspesnostRealizacije.getText());
            int brojDodeljenih=Integer.parseInt(txtBrojDodeljenihKlijenata.getText());
            Agent noviAgent = new Agent(broj,ime,prezime,adresa,uspesnost,brojDodeljenih);
            UpdateAgent updAgent = new UpdateAgent(noviAgent, agent.getRedniBrojURegistruPosrednika());
            boolean signal = Controller.getInstance().updateAgent(updAgent);
            if(signal){
                JOptionPane.showMessageDialog(this, "Sistem je zapamtio agenta.", "Izmena agenta", JOptionPane.INFORMATION_MESSAGE);
            }else{
                 JOptionPane.showMessageDialog(this, "Sistem ne može da zapamti agenta.", "Izmena agenta", JOptionPane.WARNING_MESSAGE);
           
            }
        } catch (Exception ex) {
            Logger.getLogger(SelektovaniAgent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

