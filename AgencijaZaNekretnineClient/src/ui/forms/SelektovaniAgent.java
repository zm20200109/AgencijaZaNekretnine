    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        try {
            // TODO add your handling code here:
            btnIzmeni.setVisible(false);
            boolean signal = Controller.getInstance().deleteAgent(this.agent);
            if(signal){
                JOptionPane.showMessageDialog(this, "Sistem je izbrisao agenta.", "Brisanje agenta", JOptionPane.INFORMATION_MESSAGE);
                this.getTopLevelAncestor().setVisible(false);
            }else{
                  JOptionPane.showMessageDialog(this, "Sistem ne može da obriše agenta.", "Brisanje agenta", JOptionPane.INFORMATION_MESSAGE);
           
            }
            
        } catch (Exception ex) {
            Logger.getLogger(SelektovaniAgent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnObrisiActionPerformed


