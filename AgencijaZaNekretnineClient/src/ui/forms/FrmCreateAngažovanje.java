    private void popuniFormuPodacima() throws Exception {
        System.out.println("Usao u popuni formu podacima");
        
         txtMenadzer.setText(menadzer.getImeMenadzera()+" "+menadzer.getPrezimeMenadzera());
        txtMenadzer.setEnabled(false);
        
        
        System.out.println("Setova txtMenadzer i onemogucio izmenu");
        
        
        
        List<Agent> agenti = Controller.getInstance().getAllAgenti();
        System.out.println("Vratio listu agenata iz baze");
        for (Agent a: agenti){
            cbAgent.addItem(a);
        }
        
        
        List<Ugovor> ugovori = Controller.getInstance().getAllUgovori();
        
        for(Ugovor u: ugovori){
            cbUgovori.addItem(u);
        }
        
        
    }
