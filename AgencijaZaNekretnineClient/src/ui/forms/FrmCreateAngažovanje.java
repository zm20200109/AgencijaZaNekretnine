    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        try {
            // TODO add your handling code here:
            double provizija = Double.parseDouble(txtProvizija.getText());
            long redniBroj = Long.parseLong(txtRedniBrojAngazovanja.getText());
            boolean realizovano = false;
            if(txtRealizovano.getText().equals("DA")){
                realizovano=true;
            }else{
                realizovano = false;
            }
            Agent a = (Agent) cbAgent.getSelectedItem();
            Ugovor u = (Ugovor) cbUgovori.getSelectedItem();
            Angazovanje angažovanje = new Angazovanje(redniBroj,provizija,realizovano,u,a, menadzer);
            boolean signal = Controller.getInstance().createAngazovanje(angažovanje);
            if(signal){
                JOptionPane.showMessageDialog(this, "Sistem je uspešno uneo angažovanje.","Unos angažovanja",JOptionPane.INFORMATION_MESSAGE);
                this.getTopLevelAncestor().setVisible(false);
            }else{
                JOptionPane.showMessageDialog(this, "Sistem nije uspeo da unese angažovanje.","Unos angažovanja",JOptionPane.INFORMATION_MESSAGE);
                
            }
        } catch (Exception ex) {
            Logger.getLogger(FrmCreateAngažovanje.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnSacuvajActionPerformed

